				<h1 itemprop="headline">Introducing the Berlingske family</h1>
				<div class="body" itemprop="articleBody">
					<p>
						After four years of hard work Playtype now proudly present the grandest typeface ever made in the history of Danish design.
						<br /><br />
						The typeface Berlingske, was originally designed by Jonas Hecksher in 2010 for the Danish daily Berlingske. Today, after four years of further develop with extended styles, language and weights, it represents a wide ranged family that delivers freedom to play around and give any section or subject area a specific tonality without sacrificing consistency. The resulting font family comes in more 227 weights and contains almost half a million different glyphs. The Berlingske design is carefully balanced to deliver significant modernization while paying homage to a unique heritage. The typeface is crafted to give a smooth and exclusive look that optimizes the visual experience: providing a clear and sharp readability while being aesthetically pleasing.
						<br /><br />
						<strong>Varius expressions</strong><br />
						Berlingske was originally designed as a distinctly newspaper typography, where the difference in height between capitals and minuscule has to be significant in order for the text to be immediately decodable. Today the selection of weights ranges from elegant thin to an expressive poster and fits for body text, display and headlines. The amount of styles embraces a wide range of section and subject area, spanning from news, fashion, art, politics, economic, society, culture, sport and to lifestyle. Berlingske is eminently readable and strikingly easy to navigate in while rich in identity and immediately recognizable.
						<br /><br />
						<strong>Tremendous in volume</strong><br />
						The first Berlingske design contained 21 weights customized for the newspaper Berlingske. After further develop with extended styles, language and weights, the typeface Berlingske today represents a wide ranged family with 227 different weights, each with 2100 glyphs, in total 476.700 Glyphs. Berlingske is designed by Jonas Hecksher, Playtype forundry, and it is so far the largest type family ever designed by a Danish designer.
						<br /><br />					
						<strong>Worldwide</strong><br />
						Berlingske is not only a broad ranged family in styles but is also compatible for 170 different languages, provides coverage for both Western and Eastern European languages plus Cyrillic, Greek, Greek Extended, Latin Extended Additional, Baltic & Vietnamese. The amount of styles and languages makes is suitable for use all kinds of purpose, all over the world.
						<br /><br />
