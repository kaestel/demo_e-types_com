				<div class="body" itemprop="articleBody">
					<p>
						e-Types has developed and designed a brand new sustainability website for The ROCKWOOL Group.
						<br /><br />
						ROCKWOOL is moving away from mainly communicating sustainability via reports and wishes to target a broader audience through digital media. The task was to create a sustainability communication concept which is relevant to not only specialists but all ROCKWOOL main target groups; developers, specifiers, contractors, distributors and in this case particularly home owners.
						<br /><br />
						Taking its outset in ROCKWOOL’s five main focus areas; Environment, Social Impact, Health, Fire-Safety and Energy Efficiency, the website allows for people to dive into the ROCKWOOL universe. It lets them intuitively experience and understand the connection between the ‘big issues’ and how products from across The ROCKWOOL Group are part of the solution of making the planet a better place.
						<br /><br />
						As part of the process, the site was launched internally in the autumn of 2014, where all ROCKWOOL employees were invited to contribute with facts and knowledge. Also the soft launch was used to educate staff in ROCKWOOL sustainability knowledge within the organisation.
