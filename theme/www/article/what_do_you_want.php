				<div class="body" itemprop="articleBody">
					<p>
						Again this year, e-Types has created a campaign for Roskilde University, aiming to recruit new students in 2015. The campaign, 'Hvad vil du?’ or 'What do you want? portrays seven bachelor students from RUC attending seven different educational programs, ranging from Philosophy to Physics.
						<br /><br />
						The campaign is based on seven posters, showing the students in their study environment at RUC. Through quotes and visual elements, each portrait tells a story about project work, interdisciplinary studies and life at campus. On the campaign website, the profiles and their stories are further unfolded in seven full interviews.
						<br /><br />
						Graphically, the campaign follows the overall concept of last years campaign, maintaining the bright green color frame, the full body portraits and the centered white typography. Though this year, we portray current students, the images are more colorful and the typography speaks louder — ‘What do you want?'
