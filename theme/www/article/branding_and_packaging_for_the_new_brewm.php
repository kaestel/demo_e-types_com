				<h1 itemprop="headline">Branding and packaging for the new Brewmasters’ Collection</h1>
				<div class="body" itemprop="articleBody">
					<p>Together with Carlsberg’s innovation department, e-Types has developed the brand story and visual identity for a new range of ‘local craft’ beers. Brewmasters’ Collection is now available in Denmark, Finland and Russia and is unique for Carlsberg in recent years, seeing as it consists of completely new beer styles. The international group of breweries have long been faced with the challenge of a diminishing market for traditional beer, while the interest in craft beer continues to grow. The main task for e-Types was to ascertain how Carlsberg credibly can tap into these styles, and guide new customers into drinking new beers, at new occasions.</p>
					<p>Solving the brief consisted not only of developing the name and identity for the collection, but brand hierarchy, brand films and launch events too. e-Types Daily contributed with packaging design, POS material and merchandise. Brewmasters’ Collection is all about the love for quality beer and the legacy of the brewmasters of old. In celebration of their heritage, a new Board of Brewers was formed, including representatives from several countries. The new IPA, Irish red ale and California steam beer come from their recipes, and the brewmasters’ stories are incorporated in the can design. The design has a 360-degree application that makes it recognisable from all angles and maximizes shelf impact.</p>
