				<h1 itemprop="headline">New commissioned typeface for Skagen Denmark</h1>
				<div class="body" itemprop="articleBody">
					<p>
						The wristwatch and accessories brand, Skagen Denmark, which is a part of the global brand and design manufacturer <a href="https://www.fossilgroup.com" target="_blank">Fossil</a>, was founded by Danes in the US in 1989. Despite the fact the company is based in Dallas, the Skagen Denmark states that they are “determined to bring Danish-inspired ideals to the world, through design imbued with purpose, honesty and simplicity” – one of the main reasons they connected with e-Types.
						<br /><br />
						Skagen Denmark had a desire to capture this same characteristic Scandinavian style to express the Danish tradition within their brand. To understand the essence of Skagen – and to be able to add that particular Danish appearance to this new typeface, the e-Types team went to the city of Skagen to feel the atmosphere and to find inspiration in its architecture and nature.
						<br /><br />
						Specifically Danish<br />
						What they found was some of the essence of Skagen itself: The light, which has drawn so many creative minds up north, but also Thorvald Bindesbøll’s wooden buildings by the harbour, the hand-drawn names and numbers on fishing boats, the flat, yet undulating landscape.
						<br />
						“We searched for elements that could serve as a starting point for the development of a visual language. In this case even the distinct shape of Skagen’s hook into the ocean itself became a source of inspiration,” say Hecksher and Kajus.
						<br />
						One distinct quality of Danish design – its timelessness – is repeated in the typographic endeavors of Danish designers. The search for the essential function, and the form to go with it, means that generally the types are able stand the test of time. Thus, older Danish typographies have been essential inspiration for the Skagen typeface. “Form follows function, also in Danish typography design. Danish types are often quite simple; there is this common search for the essence and a desire to strip away what’s not strictly necessary. Interestingly enough, they are also often built around geometric principles,” says Hecksher and Kajus.
						<br />
						“A key element in our research was spending time in the archives, investigating the works of icons such as Claus Achton Friis and Knud V. Engelhardt. We wanted a distinctly Danish look & feel,” they explain. From now on, Skagen has its own corporate typeface consisting of five extended sans-serif weights, which truly represents the company’s ethos.
					</p>
