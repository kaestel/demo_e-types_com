					<p>
						No other means of transportation is as suited for the future as the Metro is. Fast, safe, easy on the environment and free from old-world-style tedious schedule. A new campaign by e-Types showcases all its possibilities.
						<br /><br />
						In the coming years the Copenhagen Metro is to more than double its impact in number of passengers and neighborhoods served.
						<br /><br />
						To support the idea that the Metro is – and increasingly will be – an as integrated a part of the city as running water, electricity, roads and bicycle paths, we created the campaign: Til byen. It roughly translates to both 'to the city' and 'for the city'. The campaign shows all the great things the city offers you – and that the Metro takes you there.
						<br /><br />
						We created 21 posters for the campaign, and to support the message we came up with the idea to create the site <a href="http://www.tilbyen.dk" target="_blank">tilbyen.dk</a> where your hashtagged "tilbyen" images will show up.
