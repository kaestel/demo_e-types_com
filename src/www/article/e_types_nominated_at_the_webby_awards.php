				<h1 itemprop="headline">e-Types nominated at The Webby Awards</h1>
				<div class="body" itemprop="articleBody">
					<p>
						<a href="http://www.soulland.com" target="_blank">soulland.com</a> has been nominated at the prestigious Webby Awards 2014 that honours digital excellence.
						<br /><br />
						We are pleased to announce, that the website we designed and developed for one of Denmarks leading fashion brands, Soulland, has been nominated for ‘Best website’ in the ‘Fashion & Beauty’ category. Among the other nominees are some of the worlds biggest e-commerce fashion sites like net-a-porter.com and mr-porter.com.
						<br /><br />
						Jakob Kampp Berliner, CEO at Soulland, says: “We are very proud to be nominated in this category. Together with e-Types we have put a great deal of energy and effort into rethinking the way we present our brand and our products to our customers. The other nominees count some of the biggest e-commerce websites on the market, so I guess we must have done something right.”
						<br /><br />
						Frederik Bjerg Kirketerp, Head of Digital Design at e-Types: “In our line of work getting a nomination from The Webbys Awards is one of the highest acknowledgements you can get, so we are very happy. We set out to make a website for Soulland with a main focus on e-commerce and their products, but we also wanted a website that embraces who they are as a brand and how they showcase themselves on other platforms. Among other things, we wanted the user to have a hands-on-experience with the products – as much as you can, when you are sitting behind a screen – so they could sense the high quality and level of detail that reflects the Soulland products.”
						<br /><br />
						The Webby Awards is the leading international award honoring excellence on the Internet. Established in 1996 during the Web's infancy, The Webbys is presented by the International Academy of Digital Arts and Sciences (IADAS) — a 1,000+ member judging body that includes Executive Members comprised of leading Web experts, business figures, luminaries, visionaries and creative celebrities, and Associate Members who are former Webby Award Winners and Nominees and other Internet professionals.
						<br /><br />
						The Academy is an intellectually diverse organization that includes members such as actor Kevin Spacey, musician David Bowie, Internet Co-inventor Vint Cerf, Arianna Huffington, President and Editor-in-Chief of Huffington Post Media Group, Twitter Co-founder Biz Stone, Chairman and Founder of R/GA Bob Greenberg, Co-Founder of Instagram Kevin Systrom, Executive Creative Director at Google Lab, Iain Tait, Mozilla CEO and Chair Mitchell Baker, Tumblr Founder David Karp, and Roy L. Furman Professor of Law and Leadership at Harvard Law School, Lawrence Lessig. The 18th Annual Webby Awards received 12,00 entries from over 60 countries and all 50 states and generated over 900 million media impressions worldwide.
